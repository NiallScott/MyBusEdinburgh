package uk.org.rivernile.android.bustracker.ui.theme

import androidx.compose.foundation.isSystemInDarkTheme
import androidx.compose.material3.MaterialTheme
import androidx.compose.material3.darkColorScheme
import androidx.compose.material3.lightColorScheme
import androidx.compose.runtime.Composable

/**
 * The base theme to be used on the MyBus* apps.
 *
 * @param useDarkTheme Whether dark theme should be used or not. This defaults to the value returned
 * from [isSystemInDarkTheme].
 * @param content The [Composable] content within this theme.
 */
@Composable
fun MyBusTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colors = if (useDarkTheme) {
        DarkColors
    } else {
        LightColors
    }

    MaterialTheme(
        colorScheme = colors,
        content = content
    )
}

